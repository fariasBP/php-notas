<?php 
/* BUCLE FOR
	Un bucle for se repite hasta que la condición especificada se evalúa como false
	TEMAS:
		- for
	FORMA DE ESCRITURA:
		for ([expresion inicial], [condicion], [operador de incremento|decremento]) {
			#instruccion que itera hasta que la condicion sea false
		}
*/
for ($i = 0; $i <= 10; $i++) {
    echo ($i);
}
for ($i = 10; $i>=0; $i--) {
    echo ($i);
}