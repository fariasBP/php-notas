<?php
/* OPERADORES ARITMETICOS
	son aquellos operadores que manipulan datos con expresiones Aritmeticas
	TEMAS:
		- + ---> adicion
		- - ---> sustraccion
		- * ---> productos
		- / ---> divisiones
*/

define("numero_a", 10);
define("numero_b", 6);
define("numero_c", 7);

// Adicion
	echo (numero_a + numero_b);
	echo "<br>";
// Sustraccion
	echo (numero_b - numero_c);
	echo "<br>";
// multiplicacion
	echo (numero_c * numero_a);
	echo "<br>";
// Divisiones
	echo (numero_a / numero_b);