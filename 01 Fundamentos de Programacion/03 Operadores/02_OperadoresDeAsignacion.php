<?php 
/* OPERADORES DE ASIGNACION
	Son aquellos operadores que asignan valores
	TEMAS:
		- = ---> Asignacion 
		- += ---> Asignacion mas
		- -= ---> Asignacion menos
		- *= ---> Asignacion por
		- /= ---> Asignacion dividido
		- %= ---> Asignacion de resto
*/

// Asignacion
	// - da un valor nuevo
	$mi_variable = 10;
	echo $mi_variable;
	echo "<br>";
// Asignacion mas
	// - aumenta su valor
	$mi_variable += 5;
	echo $mi_variable;
	echo "</br>";
// Asignacion menos
	// - disminuye su valor
	$mi_variable -= 3;
	echo $mi_variable;
	echo "<br>";
// Asignacion por
	// - multiplica su valor
	$mi_variable *= 2;
	echo $mi_variable;
	echo "<br>";
// Asignacion dividido
	// - divide su valor
	$mi_variable /= 4;
	echo $mi_variable;
	echo "<br>";
// Asigancion de Resto
	// - Halla el resto de una division
	$mi_variable %= 4;
	echo $mi_variable;
	echo "<br>";